def create_image_directory():
    """Create images directory if it doesn't exist"""
    image_dir = "images"
    if not os.path.exists(image_dir):
        os.makedirs(image_dir)
        print(f"Created directory: {image_dir}")
    return image_dir

def generate_image(prompt, image_id, image_dir):
    """Generate a single image using DALL-E 3"""
    try:
        filename = f"scene_{image_id:02d}.png"
        filepath = os.path.join(image_dir, filename)
        
        # Check if image already exists
        if os.path.exists(filepath):
            print(f"â­ï¸  Skipping image {image_id}/20: {filename} already exists")
            return {"status": "skipped", "filepath": filepath}
            
        print(f"ðŸŽ¨ Generating image {image_id}/20: {prompt[:60]}...")
        
        # Generate image using DALL-E 3
        response = client.images.generate(
            model="dall-e-3",
            prompt=prompt,
            n=1,
            size="1024x1024",
            quality="standard",
            style="natural"
        )
        
        # Get the image URL
        image_url = response.data[0].url
        
        # Download the image
        image_response = requests.get(image_url)
        if image_response.status_code == 200:
            with open(filepath, "wb") as f:
                f.write(image_response.content)
            
            print(f"âœ… Saved: {filename}")
            return {"status": "generated", "filepath": filepath}
        else:
            print(f"âŒ Failed to download image {image_id}")
            return {"status": "failed", "filepath": None}
            
    except Exception as e:
        print(f"âŒ Error generating image {image_id}: {str(e)}")
        return {"status": "failed", "filepath": None}

